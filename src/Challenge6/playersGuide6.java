/* Objectives:

Build a program with a variable of all eight primitive Java types (as well as the ‘text’ type).
Assign each of them a value using a literal of the correct type.
Use System.out.println to display the contents of each variable */
package Challenge6;

public class playersGuide6 {
    public static void main (String[] args) {

    int interger = 4;
    byte bYte = 1;
    short kort = 2;
    long lang = 8;
    float drijf = 9.995f;
    double dubbel = 15;
    boolean boo = true;
    char kar = 'a';
    String text = "variableShop";

    System.out.println( "a int is: " + interger);
    System.out.println(" a byte is:"+ bYte);
    System.out.println(" a short is" + kort);
    System.out.println(" a long is" + lang);
    System.out.println(" a float is" + drijf);
    System.out.println(" a double is" + dubbel);
    System.out.println ( " a boolean is" + boo);
    System.out.println ( " a char is" + kar);
    System.out.println ( " a string is" + text);

    }
}
